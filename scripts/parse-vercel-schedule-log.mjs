#!/usr/bin/env node
/**
 * Vercel ログ JSON を読み、POST /api/schedule から復元用データを集約する。
 *
 * 対応:
 * 1) [api/schedule] shiftSummary {"u":"...","rows":[{"d":"YYYY-MM-DD","a":"HH:mm","b":"HH:mm","a2":"","b2":""},...]}
 * 2) [api/schedule] attempting first-shift Slack { targetUserId, firstWorkDateLabel, timeRangeLabel }（1日分のみ）
 *
 * 同一 user_id + 日付は timestampInMs が最大のものを採用。
 * 行の a/b が「なし」または空のものは「有効な時間データ」として捨てる。
 *
 * 使い方:
 *   node scripts/parse-vercel-schedule-log.mjs /path/to/log-export.json > restore.sql
 */
import fs from "fs";

const path = process.argv[2];
if (!path) {
  console.error("Usage: node scripts/parse-vercel-schedule-log.mjs <vercel-log.json>");
  process.exit(1);
}

const raw = fs.readFileSync(path, "utf8");
const rows = JSON.parse(raw);

/** @type {Map<string, { userId: string, date: string, s1: string, e1: string, s2: string, e2: string, ts: number, source: string }>} */
const best = new Map();

function key(userId, date) {
  return `${userId}\t${date}`;
}

function isConcretePair(s1, e1) {
  const a = String(s1 ?? "").trim();
  const b = String(e1 ?? "").trim();
  if (!a || a === "なし") return false;
  if (!b || b === "なし") return false;
  return true;
}

function consider(userId, date, s1, e1, s2, e2, ts, source) {
  if (!userId || !date || !isConcretePair(s1, e1)) return;
  const k = key(userId, date);
  const prev = best.get(k);
  if (!prev || ts >= prev.ts) {
    best.set(k, {
      userId,
      date,
      s1: String(s1).trim(),
      e1: String(e1).trim(),
      s2: String(s2 ?? "").trim() || "",
      e2: String(e2 ?? "").trim() || "",
      ts,
      source,
    });
  }
}

for (const r of rows) {
  if (!String(r.requestPath || "").includes("/api/schedule")) continue;
  if (r.requestMethod !== "POST") continue;
  const ts = Number(r.timestampInMs) || 0;
  const msg = String(r.message || "");

  if (msg.includes("[api/schedule] shiftSummary")) {
    const idx = msg.indexOf("shiftSummary");
    const jsonPart = msg.slice(idx + "shiftSummary".length).trim();
    try {
      const j = JSON.parse(jsonPart);
      const uid = j.u;
      const list = j.rows || [];
      for (const row of list) {
        consider(uid, row.d, row.a, row.b, row.a2, row.b2, ts, "shiftSummary");
      }
    } catch {
      /* ignore */
    }
    continue;
  }

  if (msg.includes("attempting first-shift Slack")) {
    const uid = msg.match(/targetUserId:\s*'([^']+)'/)?.[1];
    const label = msg.match(/firstWorkDateLabel:\s*'([^']+)'/)?.[1];
    const tr = msg.match(/timeRangeLabel:\s*'([^']+)'/)?.[1];
    if (!uid || !label || !tr) continue;
    const dm = label.match(/(\d{4})年(\d{2})月(\d{2})日/);
    if (!dm) continue;
    const date = `${dm[1]}-${dm[2]}-${dm[3]}`;
    const tm = tr.split(/\s*〜\s*/);
    const s1 = (tm[0] || "").trim();
    const e1 = (tm[1] || "").trim();
    consider(uid, date, s1, e1, "", "", ts, "first-shift-Slack");
  }
}

const sorted = Array.from(best.values()).sort((a, b) => {
  if (a.userId !== b.userId) return a.userId.localeCompare(b.userId);
  return a.date.localeCompare(b.date);
});

console.error(`Parsed ${path}: ${sorted.length} user×date rows with concrete times.`);

console.log("-- Auto-generated by scripts/parse-vercel-schedule-log.mjs");
console.log("BEGIN;\n");

for (const row of sorted) {
  const uid = row.userId;
  const d = row.date;
  const s1 = row.s1.replace(/'/g, "''");
  const e1 = row.e1.replace(/'/g, "''");
  const s2 = row.s2 ? row.s2.replace(/'/g, "''") : "";
  const e2 = row.e2 ? row.e2.replace(/'/g, "''") : "";

  const s2sql = row.s2 && row.e2 ? `'${s2}'` : "NULL";
  const e2sql = row.s2 && row.e2 ? `'${e2}'` : "NULL";

  console.log(
    `UPDATE public.shifts SET start_planned = '${s1}', end_planned = '${e1}', start_planned2 = ${s2sql}, end_planned2 = ${e2sql} WHERE user_id = '${uid}'::uuid AND date = '${d}'::date AND TRIM(start_planned) IN ('なし', '') AND TRIM(end_planned) IN ('なし', '');`
  );

  console.log(
    `INSERT INTO public.shifts (id, user_id, date, start_planned, end_planned, start_planned2, end_planned2) SELECT gen_random_uuid(), '${uid}'::uuid, '${d}'::date, '${s1}', '${e1}', ${s2sql}, ${e2sql} WHERE NOT EXISTS (SELECT 1 FROM public.shifts s WHERE s.user_id = '${uid}'::uuid AND s.date = '${d}'::date);`
  );
  console.log("");
}

console.log("COMMIT;");
